class Post(models.Model):
    class Status(models.TextChoices):
        DRAFT = 'DRAFT', 'Draft'
        SCHEDULED = 'SCHEDULED', 'Scheduled'
        PUBLISHED = 'PUBLISHED', 'Published'
        GENERATION_FAILED = 'FAILED', 'Generation Failed'
        PUBLISH_FAILED = 'PUBLISH_FAILED', 'Publish Failed' 

    campaign = models.ForeignKey(
        Campaign, on_delete=models.CASCADE, related_name='posts')
    keyword = models.ForeignKey(
        Keyword, on_delete=models.SET_NULL, null=True, blank=True)
    integration_target = models.ForeignKey(
        Integration, on_delete=models.SET_NULL, null=True)
    meta_description = models.TextField(blank=True, null=True)
    tags = models.JSONField(default=list, blank=True,
                            help_text="List of tags for the post.")
    categories = models.JSONField(
        default=list, blank=True, help_text="List of category IDs for the post.")

    title = models.CharField(max_length=255)
    content = models.TextField()
    status = models.CharField(
        max_length=20, choices=Status.choices, default=Status.DRAFT)

    publish_at = models.DateTimeField()
    published_url = models.URLField(blank=True, null=True)
    error_message = models.TextField(blank=True, null=True)

    created_at = models.DateTimeField(auto_now_add=True)
    updated_at = models.DateTimeField(auto_now=True)

    featured_image_url = models.URLField(blank=True, null=True, help_text="Original image URL from media settings")
    wordpress_media_id = models.IntegerField(blank=True, null=True, help_text="WordPress media library ID after upload")
    wordpress_image_url = models.URLField(blank=True, null=True, help_text="WordPress media library URL")

    @property
    def is_failed(self):
        """Returns True if the post is in any failed state."""
        return self.status in [self.Status.GENERATION_FAILED, self.Status.PUBLISH_FAILED]

    class Meta:
        ordering = ['-created_at']

    def __str__(self):
        return self.title




class CampaignLog(models.Model):
    """Stores a log entry related to a specific campaign run."""
    class LogLevel(models.TextChoices):
        INFO = 'INFO', 'Info'
        WARNING = 'WARNING', 'Warning'
        ERROR = 'ERROR', 'Error'
        DEBUG = 'DEBUG', 'Debug'

    id = models.UUIDField(primary_key=True, default=uuid.uuid4, editable=False)
    campaign = models.ForeignKey(
        Campaign, on_delete=models.CASCADE, related_name='logs')
    post = models.ForeignKey(
        Post, on_delete=models.SET_NULL, null=True, blank=True)

    created_at = models.DateTimeField(auto_now_add=True)
    level = models.CharField(
        max_length=10, choices=LogLevel.choices, default=LogLevel.INFO)
    node_name = models.CharField(max_length=100, blank=True)
    message = models.TextField()

    class Meta:
        ordering = ['-created_at']

    def __str__(self):
        return f"[{self.created_at.strftime('%Y-%m-%d %H:%M')}] [{self.level}] {self.message[:80]}"
