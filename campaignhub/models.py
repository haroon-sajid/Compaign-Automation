# --- Main Campaign Model ---
class PostStatusChoices(models.TextChoices):
    DRAFT = 'DRAFT', 'Draft'
    SCHEDULED = 'SCHEDULED', 'Scheduled'


class Campaign(models.Model):
    class CampaignType(models.TextChoices):
        BLOG = 'BLOG', 'Blog'
        SOCIAL = 'SOCIAL', 'Social'

    class Status(models.TextChoices):
        DRAFT = 'DRAFT', 'Draft'
        QUEUED = 'QUEUED', 'Queued'
        ACTIVE = 'ACTIVE', 'Active'
        COMPLETED = 'COMPLETED', 'Completed'
        ARCHIVED = 'ARCHIVED', 'Archived'

    id = models.UUIDField(primary_key=True, default=uuid.uuid4, editable=False)
    organization = models.ForeignKey(
        Organization, on_delete=models.CASCADE, related_name='campaignhub')
    created_by = models.ForeignKey(
        settings.AUTH_USER_MODEL, on_delete=models.SET_NULL, null=True, related_name='campaignhub')

    name = models.CharField(max_length=255)
    status = models.CharField(
        max_length=10, choices=Status.choices, default=Status.QUEUED)

    # ADD THIS FIELD to your model
    campaign_type = models.CharField(
        max_length=10, choices=CampaignType.choices, default=CampaignType.BLOG)
    post_creation_status = models.CharField(
        max_length=10,
        choices=PostStatusChoices.choices,
        default=PostStatusChoices.SCHEDULED,
        help_text="The initial status for posts generated by the agent for this campaign."
    )

    created_at = models.DateTimeField(auto_now_add=True)
    updated_at = models.DateTimeField(auto_now=True)

    def __str__(self):
        return f"{self.name} ({self.get_campaign_type_display()} - {self.organization.name})"

# --- Models for Wizard Steps ---


class PromptTemplate(models.Model):
    id = models.UUIDField(primary_key=True, default=uuid.uuid4, editable=False)
    organization = models.ForeignKey(
        Organization, on_delete=models.CASCADE, related_name='prompt_templates')
    created_by = models.ForeignKey(
        settings.AUTH_USER_MODEL, on_delete=models.SET_NULL, null=True)

    name = models.CharField(
        max_length=255, help_text="A unique name for this prompt template.")

    campaign_type = models.CharField(
        max_length=10,
        choices=Campaign.CampaignType.choices,
        default=Campaign.CampaignType.BLOG
    )

    system_prompt = models.TextField(blank=True, null=True)
    user_prompt = models.TextField(blank=True, null=True)

    created_at = models.DateTimeField(auto_now_add=True)
    updated_at = models.DateTimeField(auto_now=True)

    class Meta:
        unique_together = ('organization', 'name', 'campaign_type')
        ordering = ['-created_at']

    def __str__(self):
        return f"{self.name} ({self.get_campaign_type_display()})"


